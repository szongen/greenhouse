/**
 * @brief 查找汉字编码位置
 * @param  lo 汉字寻码低位地址              
 * @param  hi 汉字寻码高位地址               
 * @return uint16_t 返回汉字坐标
 */
static uint16_t SearchhzIndex(uint8_t  lo, uint8_t  hi) 
{
    uint16_t i = 0;

    for (i = 0; i < sizeof(GB_16)/sizeof(typFNT_GB16); i++)
    {
            if ((GB_16[i].Index[0] == lo) && (GB_16[i].Index[1] == hi))
            {
                    return i;
            }
    }
    return 0;
}
/** 
 * @brief 显示16*8的汉字字符
 * @param  x 起始x坐标               
 * @param  y 起始y坐标              
 * @param  text 显示字符串            
 */
static void Display_Graphic_16x16(uint8_t x,uint8_t y,const char *text)
{
	uint8_t i,j,temp,mi,z=0;
	uint8_t font=SearchhzIndex(*text, *(text + 1));//汉字寻址
	for (i=0;i<16;i++)
	{	
		for(j=0;j<2;j++,z++)
		{
			for(temp=0x01,mi=0;mi<8;mi++)
			{
				if(temp&GB_16[font].Msk[z])
					Oled_Draw_Point(x+i,y+mi+8*j);
				temp<<=1;							
			}
		}
	}
}

/** 
 * @brief 显示16*8的ascll字符
 * @param  x 起始x坐标               
 * @param  y 起始y坐标              
 * @param  text 显示字符串            
 */
static void Display_String_16x8(uint8_t x,uint8_t y,char const  *text)
{
	uint8_t i,j,temp,mi,z=0,n;

	n=text[z]-0x20;

	for (i=0;i<8;i++)
	{	
		for(j=0;j<2;j++,z++)
		for(temp=0x01,mi=0;mi<8;mi++)
		{
			if(temp&ascii_table_16x8[n][z])
			Oled_Draw_Point(x+i,y+mi+8*j);
			temp<<=1;							
		}		
	}
}
/** 
 * @brief 显示16*16的字符
 * @param  x 起始x坐标               
 * @param  y 起始y坐标              
 * @param  text 显示字符串            
 */
void Oled_Disp_String(uint8_t x,uint8_t y,char const  *text)
{
	while (*text)
	{			
		if ((*text) < (char)0x80)
		{
			Display_String_16x8(x,y,text);
			x += 8;
			text++;
		}
		else  if (((*text) &0xf0) && ((*(text + 1)) &0xf0))
		{
			Display_Graphic_16x16(x,y,text);
			x += 16;
			text++;
			text++;
		}	
		else
		{
		text++;
		}
	}
}

/**
 * @brief 反显区域
 * @param  x  起始x坐标               
 * @param  y  起始y坐标                 
 * @param  kuan 反显长度            
 * @param  gao  反显宽度            
 */
void Interfacr_Shadow(uint8_t x,uint8_t y,uint8_t kuan,uint8_t gao)
{
	uint8_t i,j;
	for(i=0;i<kuan;i++)
	{	
		for(j=0;j<gao;j++)
		{
			if(OLED_DATA_BUF[x+i][(y+j)/8]&(0x01<<((y+j)%8)))
				OLED_DATA_BUF[x+i][(y+j)/8]&=(~(0xff&(0x01<<((y+j)%8))));//写0
			else
				OLED_DATA_BUF[x+i][(y+j)/8]|=(0x01<<((y+j)%8));//写1
		}
	}
}

